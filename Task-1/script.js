let a = document.querySelector(".input");
function add(x){
    a.value += x;
}
function plus(){
    const adds = "+";
    a.value += adds ;
}
function minus(){
    const subs = "-"
    a.value += subs;
}
function mul(){
    const muls = "*"
    a.value += muls;
}
function div(){
    const divs = "/"
    a.value += divs;
}